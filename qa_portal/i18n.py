def normalize_language(language: str | None) -> str:
    normalized = (language or DEFAULT_LANGUAGE).strip().lower()
    return normalized if normalized in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translate(language: str | None, text: str, **kwargs: object) -> str:
    lang = normalize_language(language)
    template = TRANSLATIONS.get(lang, {}).get(text, text)
    try:
        return template.format(**kwargs)
    except (IndexError, KeyError, ValueError):
        return template


def translate_value(language: str | None, category: str, value: object) -> str:
    lang = normalize_language(language)
    raw = "" if value is None else str(value)
    mapping = VALUE_LABELS.get(category, {})
    return mapping.get(lang, {}).get(raw, mapping.get(DEFAULT_LANGUAGE, {}).get(raw, raw))


def build_ui_i18n(language: str | None) -> dict[str, object]:
    lang = normalize_language(language)
    labels = {
        category: values.get(lang, values.get(DEFAULT_LANGUAGE, {}))
        for category, values in VALUE_LABELS.items()
    }
    return {
        "lang": lang,
        "messages": TRANSLATIONS.get(lang, {}),
        "labels": labels,
    }


def resolve_language_from_mapping(data: Mapping[str, str | None]) -> str:
    return normalize_language(data.get("lang"))
